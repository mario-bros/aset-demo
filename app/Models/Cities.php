	public static function getCity($getID=0)
	{
	    if (Session::has('kota'))
		{
	        $cityOptions=Session::get('kota');
	        $cityIds=Session::get('kotaid');
	    }
	    else
	    //if (empty(session::get('kota')))
	    {
    	    $curl = curl_init();
    	    $header[] = "key:".env('RAJAONGKIR_KEY');

    	    curl_setopt_array($curl, array(
    	        CURLOPT_URL => env('RAJAONGKIR_BASEURL').env('RAJAONGKIR_PATH_CITY'),
    	        CURLOPT_RETURNTRANSFER => true,
    	        CURLOPT_ENCODING => "",
    	        CURLOPT_MAXREDIRS => 10,
    	        CURLOPT_TIMEOUT => 30,
    	        CURLOPT_HTTP_VERSION => CURL_HTTP_VERSION_1_1,
    	        CURLOPT_CUSTOMREQUEST => 'GET',

    	        CURLOPT_HTTPHEADER => $header,
    	    ));

    	    $response = curl_exec($curl);
    	    $err = curl_error($curl);

    	    curl_close($curl);
    	    $cityJson=json_decode($response);

    	    $cityOptions=array();
    	    $cityIds=array();
    	    foreach($cityJson->rajaongkir->results as $result){
                $cityOptions[$result->province][$result->city_name.' ['.$result->type.']']=$result->city_name.' ['.$result->type.']';
                $cityIds[$result->city_name.' ['.$result->type.']']=$result->city_id;
            }
            Session::put('kota',$cityOptions);
            Session::put('kotaid',$cityIds);
	    }

	    if ($getID==0)
	       return $cityOptions;
	    else
	       return $cityIds;
	}

